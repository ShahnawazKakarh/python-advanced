# Default Parameter Value
# Problem: Write a function that greets a user. If no name is provided, it should greet with a default name.

# Default name is set
# If name is given while printing method it will show the name else it will print default name.

def greet(name = "Shah"):
    if name:
        return name
    else:
        return name
    

# print(greet("Shahnawaz"))
print(greet())


